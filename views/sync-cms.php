        <h1>Sync News</h1>
        <p>
            Sync News is a news platform with CMS. Originally it was build as
            a school project to replace the various pin-up boards around the
            halls. The pin-up boards were the main source of information
            about events and workshops.
        </p>
        <p>
            Sync News replaced those pin-up boards. Around the school are now several
            Raspberry Pi's showing Sync News. The teachers have a single place to
            manage their workshops. The students have a single place to manage their
            subscriptions.
        </p>
        <p>
            The requirements for Sync News are pretty simple. A stack that runs PHP
            (>5.4) and some sort of database. Because we used Laravel 4.1, it supports all
            kinds of storage.
        </p>
        <p>
            For rapid front-end development we used Bootstrap. This made Sync News
            responsive as well.
        </p>
