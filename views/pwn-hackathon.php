        <h1>Team Bubbles and PWN hack-a-thon</h1>

        <p>
            On the 25th of September I took part in a 3-day hack-a-thon.
            (For those who don't know, a hack-a-thon is a contest where
            the participants convert coffee to a product or concept.)
        </p>

        <p>
            Out of 6 teams that started, we won!
        </p>

        <p>
            The contest was organized by PWN, a water company in
            collaboration with a 3d-printing company. The goal
            was clear: Invent something to help PWN get ready for the
            future.
        </p>

        <p>
            After some interviewing sessions with several people of PWN,
            we came to the conclusion that water pipes tend to break. A lot.
            When that happens, there is no easy way of knowing what, where and why.
        </p>

        <h2>Bubbles</h2>

        <p>
            Our solution: Bubbles. Bubbles is the name of a small robot fish.
            Equipped with several sensors, Bubbles can inspect the pipes for
            any abnormalities, in both the pipe itself and the water. The fish
            is equipped with a camera, temperature sensor and sonar.
        </p>

        <p>
            The philosophy behind Bubbles was to create a simple and cost-
            efficient "bot" that could easily inspect the pipes. Because we
            used off-the-shelve components we were able to keep prices low.
            Bubbles' hardware consists off a Raspberry-Pi and compatible
            sensors. The code is written in Python, because we believe in
            open source.
        </p>

        <p>
            The Raspberr-Pi reads the sensors and writes the data. The data is made
            accessible through bluetooth.
        </p>
